// 生成省份数据
function generateProvinceData(provinceName, factor) {
  const data = {};
  const officialData = OFFICIAL_DATA[provinceName] || {};
  
  for (let year = 1978; year <= 2025; year++) {
    // 优先使用官方数据
    if (officialData[year]) {
      data[year] = Math.round(officialData[year] / 12 * 100) / 100;
      continue;
    }
    
    // 使用全国数据 × 省份系数
    if (NATIONAL_AVG[year]) {
      const estimated = NATIONAL_AVG[year] * factor;
      data[year] = Math.round(estimated / 12 * 100) / 100;
    }
  }
  
  return data;
}

// 更新省份文件
function updateProvinceFile(provinceKey, provinceName, factor) {
  const filePath = path.join(provincesDir, `${provinceKey}.js`);
  
  if (!fs.existsSync(filePath)) {
    console.log(`文件不存在: ${filePath}`);
    return;
  }
  
  let content = fs.readFileSync(filePath, 'utf-8');
  
  // 生成新数据
  const newData = generateProvinceData(provinceName, factor);
  
  // 转换为JS对象字符串
  const dataStr = JSON.stringify(newData, null, 2)
    .replace(/"(\d+)":/g, '$1:')
    .replace(/"/g, "'");
  
  // 替换PROV_BASE对象
  const provBaseRegex = /(export const PROV_BASE = \{[\s\S]*?\});/;
  const newProvBase = `export const PROV_BASE = ${dataStr};`;
  
  if (provBaseRegex.test(content)) {
    content = content.replace(provBaseRegex, newProvBase);
  } else {
    console.log(`未找到PROV_BASE: ${provinceKey}`);
    return;
  }
  
  fs.writeFileSync(filePath, content, 'utf-8');
  console.log(`✅ 已更新: ${provinceName} (${provinceKey}.js)`);
}

